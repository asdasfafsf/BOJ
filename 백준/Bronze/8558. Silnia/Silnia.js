const input = require('fs').readFileSync(0, 'utf-8').trim()
const n = Number(input)

if (n === 0) {
    console.log(1)
} else if (n === 1) {
    console.log(1)
} else if (n === 2) {
    console.log(2)
} else if (n === 3) {
    console.log(6)
} else if (n === 4) {
    console.log(4)
} else {
    console.log(0)
}